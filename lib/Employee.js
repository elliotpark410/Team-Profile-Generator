// Create Employee class constructor (i.e. create an object with keys)
class Employee {
  constructor(name, id, email) {
    this.name = name;
    this.id = id;
    this.email = email;
  }

  getName() {
    return this.name;
  }

  getId() {
    return this.id;
  }

  getEmail() {
    return this.email;
  }

  getRole() {
    return "Employee";
  }

}

// Use Node.js module exports so other files are allowed to access the exported code
module.exports = Employee;